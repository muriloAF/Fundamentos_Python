'''Escreva um programa que leia 6 notas difer
entes e faça a média do aluno
'''
# Cadastro de variáveis
n1 = float(input('Digite a nota 1: '))
n2 = float(input('Digite a nota 2: '))
n3 = float(input('Digite a nota 3: '))
n4 = float(input('Digite a nota 4: '))
n5 = float(input('Digite a nota 5: '))
n6 = float(input('Digite a nota 6: '))
# Realização da fórmula
media = (n1 + n2 + n3 + n4 + n5 + n6) / 6
# Resposta final
print(f'A sua média final é: {media:.2f}')
